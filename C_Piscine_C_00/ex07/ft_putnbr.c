#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	str[11];
	int		idx;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{	
		write(1, "-", 1);
		nb = -nb;
	}
	idx = 10;
	while (nb != 0)
	{
		str[idx] = '0' + nb % 10;
		nb /= 10;
		idx--;
	}
	write(1, str + idx, 11 - idx);
}
