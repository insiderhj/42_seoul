long long	ft_getnum(char *str)
{
	long long	res;

	res = 0;
	while (*str)
	{
		if (*str < '0' || *str > '9')
			return (-1);
		res = res * 10 + (*str - '0');
		if (res < 0)
			return (9223372036854775807);
		str++;
	}
	return (res);
}
